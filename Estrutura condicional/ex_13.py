print('Exemplo: 1 = Domingo, 2 = segunda feira')
dia = int(input('Informe o dia em número da semana: '))

if dia == 1:
    print('Domingo')
elif dia == 2:
    print('Segunda feira')
elif dia == 3:
    print('Terça feira')
elif dia == 4:
    print('Quarta feira')
elif dia == 5:
    print('Quinta feira')
elif dia == 6:
    print('Sexta feira')
elif dia == 7:
    print('Sábado')
else:
    print('Número inválido')
